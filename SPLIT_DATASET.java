    static ArrayList<String> removeDuplicates(ArrayList<String> list) {

        ArrayList<String> result = new ArrayList<>();

        HashSet<String> set = new HashSet<>();
        city = new Vector();
        for (String item : list) {

            if (!set.contains(item)) {
                result.add(item);
                set.add(item);
                city.add(item);
            }
        }

        return result;
    }

